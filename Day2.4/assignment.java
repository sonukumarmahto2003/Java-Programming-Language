// Assignment Operators

public class assignment {
    public static void main(String[] args) {
        int a = 10;
        // a = a + 10;
        a += 10;
        System.out.println(a);

        int b = 20;
        b -= 10;
        System.out.println(b);

        int c = 30;
        c *= 2;
        System.out.println(c);

        int d = 40;
        d /= 4;
        System.out.println(d);

        int e = 50;
        e %= 2;
        System.out.println(e);
    }
}
